    private boolean salvarMetaNoBanco(String descricaoMeta, Double valorMeta) {
        try (Connection connection = ConfiguracaoBD.getConnection()) { 
            String query = "INSERT INTO metas (descricao, valor, id_usuario) VALUES (?, ?, ?)";
            try (PreparedStatement stmt = connection.prepareStatement(query)) {
                int idUsuario = SessaoUsuario.getIdUsuario();
                stmt.setString(1, descricaoMeta);
                stmt.setDouble(2, valorMeta);
                stmt.setInt(3, idUsuario);

                int resultado = stmt.executeUpdate();
                return resultado > 0; 
            }
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }

