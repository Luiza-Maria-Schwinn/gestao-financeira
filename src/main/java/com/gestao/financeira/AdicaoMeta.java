    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String descricaoMeta = tfDescricaoMeta.getText();
        String valorTexto = tfValor.getText();

        if (descricaoMeta.isEmpty() || valorTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Double valorMeta;

        try {
            valorMeta = Double.parseDouble(valorTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O valor deve ser um número!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean metaSalva = salvarMetaNoBanco(descricaoMeta, valorMeta);

        if (metaSalva) {
            JOptionPane.showMessageDialog(this, "Meta salva com sucesso!");

            if (TelaEscolhaTipoGestao.getTipoGestao().equals("Empresarial")) {
                TelaDashboardEmpresa dashboard = new TelaDashboardEmpresa();
                dashboard.atualizarMetas();
                dashboard.setVisible(true);
            } else {
                TelaDashboardPessoa dashboard = new TelaDashboardPessoa();
                dashboard.atualizarMetas();
                dashboard.setVisible(true);
            }

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar a meta no banco de dados", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

