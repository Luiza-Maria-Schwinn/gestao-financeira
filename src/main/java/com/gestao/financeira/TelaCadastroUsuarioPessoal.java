    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = tfNomeCompleto.getText().trim();
        String email = tfEmailCadastro.getText().trim();
        String telefone = tfTelefoneCadastro.getText().trim();
        String cpf = tfCPF.getText().trim();
        String senha = new String(pfSenhaCadastro.getPassword()).trim();

        if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty() || cpf.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de continuar!");
            return;
        }

        if (!email.contains("@") || !email.contains(".")) {
            JOptionPane.showMessageDialog(this, "E-mail inválido!");
            return;
        }

        if (!cpf.matches("\\d{11}")) {
            JOptionPane.showMessageDialog(this, "CPF inválido! Insira 11 dígitos numéricos.");
            return;
        }

        if (senha.length() < 6) {
            JOptionPane.showMessageDialog(this, "A senha deve ter pelo menos 6 caracteres.");
            return;
        }

        int idUsuario = salvarNoBanco(nome, email, telefone, cpf, senha);

        if (idUsuario != -1) {
            JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");

            String tipoGestao = TelaEscolhaTipoGestao.getTipoGestao();

            if ("Empresarial".equalsIgnoreCase(tipoGestao)) {
                TelaDashboardEmpresa telaDashboard = new TelaDashboardEmpresa();
                telaDashboard.setVisible(true);
            } else {
                TelaDashboardPessoa telaDashboard = new TelaDashboardPessoa();
                telaDashboard.setVisible(true);
            }

            SwingUtilities.getWindowAncestor(this).dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuário.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

