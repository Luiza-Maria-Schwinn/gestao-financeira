    private int salvarNoBanco(String nome, String email, String telefone, String cnpj, String senha) {
        String sql = "INSERT INTO usuarios (nome, email, telefone, cnpj, senha) VALUES (?, ?, ?, ?, ?)";

        try (Connection conn = ConfiguracaoBD.getConnection(); // Uso direto do método centralizado
             PreparedStatement stmt = conn.prepareStatement(sql, PreparedStatement.RETURN_GENERATED_KEYS)) {

            stmt.setString(1, nome);
            stmt.setString(2, email);
            stmt.setString(3, telefone);
            stmt.setString(4, cnpj);
            stmt.setString(5, senha);

            int linhasAfetadas = stmt.executeUpdate();

            if (linhasAfetadas > 0) {
                try (ResultSet generatedKeys = stmt.getGeneratedKeys()) {
                    if (generatedKeys.next()) {
                        return generatedKeys.getInt(1); // Retorna o ID gerado
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao acessar o banco de dados: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        return -1; // Retorna -1 em caso de falha
    }

