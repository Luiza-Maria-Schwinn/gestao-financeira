    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = tfNomeEmpresa.getText().trim();
        String email = tfEmailCadastro.getText().trim();
        String telefone = tfTelefoneCadastro.getText().trim();
        String cnpj = tfCNPJ.getText().trim();
        String senha = new String(pfSenhaCadastro.getPassword()).trim();

        if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty() || cnpj.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de continuar!");
            return;
        }

        if (!email.contains("@") || !email.contains(".")) {
            JOptionPane.showMessageDialog(this, "E-mail inválido!");
            return;
        }

        if (!cnpj.matches("\\d{14}")) {
            JOptionPane.showMessageDialog(this, "CNPJ inválido! Insira 14 dígitos numéricos.");
            return;
        }

        if (senha.length() < 6) {
            JOptionPane.showMessageDialog(this, "A senha deve ter pelo menos 6 caracteres.");
            return;
        }

        int idUsuario = salvarNoBanco(nome, email, telefone, cnpj, senha);

        if (idUsuario != -1) {
            JOptionPane.showMessageDialog(this, "Cadastro empresarial realizado com sucesso!");

            TelaDashboardEmpresa telaDashboard = new TelaDashboardEmpresa();
            telaDashboard.setVisible(true);

            SwingUtilities.getWindowAncestor(this).dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuário.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


